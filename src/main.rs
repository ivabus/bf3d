
use std::{env};
mod bf3d;

fn main() {
    let input = std::fs::read_to_string(&env::args().collect::<Vec<String>>()[1])
        .expect("Couldn't read file. Give a file with bf3d code as argument");
    bf3d::bf3d(input, true);
}

#[cfg(test)]
mod tests {
    use crate::bf3d;
    #[test]
    fn basic_hello_world(){
        assert_eq!(bf3d::bf3d(
            String::from("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+++++++++++++++++++++++++++++.+++++++..+++.-------------------------------------------------------------------------------.+++++++++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++++++++++++++++.+++.------.--------.-------------------------------------------------------------------.-----------------------."),
                              false),
                   vec!['H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd', '!', '\n'])
    }
    #[test]
    fn optimized_hello_world(){
        assert_eq!(bf3d::bf3d(
            String::from("++++++++++[>+++++++>++++++++++>+++>+<<<<-]>++.>+.+++++++..+++.>++.<<+++++++++++++++.>.+++.------.--------.>+.>."),
                              false),
                   vec!['H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd', '!', '\n'])
    }
}